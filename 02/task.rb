$opponent = {
    "A" => :Rock,
    "B" => :Paper,
    "C" => :Scissors
}

$player = {
    "X" => :Rock,
    "Y" => :Paper,
    "Z" => :Scissors
}

$outcome = {
    "X" => -1,
    "Y" => 0,
    "Z" => 1
}

$points_per_move = {
    :Rock => 1,
    :Paper => 2,
    :Scissors => 3
}

$score = {
    :Rock => {
        :Rock => 0,
        :Scissors => -1,
        :Paper => 1
    },
    :Paper => {
        :Rock => -1,
        :Paper => 0,
        :Scissors => 1
    },
    :Scissors => {
        :Rock => 1,
        :Paper => -1,
        :Scissors => 0
    }
}

def points_per_outcome (m1, m2)
    outcome = $score[m1][m2]
    score = outcome + 1
    score * 3
end

def points_per_strategy (desired)
    points = $outcome[desired]
    score = points + 1
    score * 3
end

def choose_move (m1, desired)
    required_points = $outcome[desired]
    points_table = $score[m1]
    points_table.each do | move, score |
        next unless score == required_points

        return move
    end

    return nil
end

def play_round (round)
    moves = round.split(" ")
    opponent_move_key = moves[0]
    player_move_key = moves[1]

    opponent_move = $opponent[opponent_move_key]
    player_move = $player[player_move_key]

    move_to_choose = choose_move opponent_move, player_move_key
    raise "Failed to select move #{opponent_move} -> #{player_move_key}" if move_to_choose == nil

    points_chose_moove = $points_per_move[move_to_choose]

    points_move = $points_per_move[player_move]
    points_outcome = points_per_outcome opponent_move, player_move
    points_strategy = points_per_strategy player_move_key

    #puts "#{opponent_move} - #{player_move} #{points_move} + #{points_outcome}"
    puts "#{opponent_move} - #{player_move} #{points_chose_moove} + #{points_strategy}"

    points_chose_moove + points_strategy
end

def play (game)
    sum = 0
    game.each_line do | r |
        points = play_round r
        sum = sum + points
    end

    return sum
end

